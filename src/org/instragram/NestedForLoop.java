package org.instragram;

public class NestedForLoop {
	
	public static void main(String[] args) {
		
		for (int i = 0; i <=5; i++) {
			
			for (int j = 0; j <=5; j++) {
				
				for (int k = 0; k <=5; k++) {
					
					System.out.print("a");
					
				}
				
				System.out.print("b");
				
			}
			
			System.out.println("c");
		}
		
		
		
		
		
		
	}

}
